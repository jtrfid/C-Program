#include <stdio.h>  // for 标准输入输出(I/O)库函数 
#include <stdlib.h> // for system库函数 
int main() // 主函数 
{ 
    printf("Hello World!\n" );   // 输出引号内的内容  
    system("pause");             // 保持窗口，按任意键继续，与编译器有关，有些编译器窗口不消失。 
    return 0;                    // 主函数返回值 
}
